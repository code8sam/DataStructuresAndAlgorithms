package IntroTopics;

public class VariableConventionAndRules {
    public static void main(String[] args) {
        int age = 23;
        System.out.println(age);
        int marks;
        marks = 69;
        System.out.println(marks);
        marks=71;
        System.out.println(marks);

        String NAME = "Samsung";
        System.out.println(NAME);
        String name = "S23";
        System.out.println(name);
        int modelPrice = 69000;
        System.out.println(modelPrice);

        // NOTE : Compiler doesn't assign default values to variables which are not initialised in Java.
    }
}
